"""21. Знайти добуток всіх елементів масиву дійсних чисел, менших заданого
числа. Розмірність масиву - 10. Заповнення масиву здійснити випадковими числами
від 50 до 100."""
from random import randint
A = list()
a = int(input("Задане число:"))
sum = 1
for n in range(10):
    A.append(randint(50, 100))
    # Создание масива
    if A[n] < a:
        sum *= A[n]
    # Подсчёт данных по фильтру
print(sum)