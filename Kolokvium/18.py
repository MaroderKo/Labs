"""18. Знайти добуток всіх елементів масиву цілих чисел, менших 0. Розмірність
масиву - 10. Заповнення масиву здійснити за допомогою клавіатури."""
A = list()
sum = 1
for n in range(10):
    A.append(int(input()))
    # Создание масива
    if A[n]<0:
        sum *=A[n]
    # Подсчёт данных по фильтру
print(sum)