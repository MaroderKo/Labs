"""12. Дані про температуру повітря за декаду грудня зберігаються в масиві.
Визначити, скільки раз температура була вище середньої за цю декаду."""
A = [15,18,7,4,1,-6,-10,-17,-12,-25]
# Создание масива
sum = 0
kol=0
for n in range(len(A)):
    sum+=A[n]
# вычисление суммы данных масива
sum = sum/10
# Получение среднего
for n in range(len(A)):
    if A[n]>sum:
        kol+=1
# Подсчёт по фильтру данных
print(kol)