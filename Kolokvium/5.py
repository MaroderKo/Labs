"""5. Створіть масив А [1..7] за допомогою генератора випадкових чисел і
виведіть його на екран. Збільште всі його елементи в 2 рази."""
from random import randint
A = list()
for n in range(7):
    A.append(randint(0,100))
#Создание масива
print(A)
for n in range(7):
    A[n] = A[n]*2
