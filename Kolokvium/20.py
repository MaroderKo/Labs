"""20. Знайти суму всіх елементів масиву дійсних чисел, більших за задане
число. Розмірність масиву - 20. Заповнення масиву здійснити випадковими числами
від 50 до 100."""
from random import randint
A = list()
a = int(input("Задане число:"))
sum = 0
for n in range(20):
    A.append(randint(50, 100))
    # Создание масива
    if A[n] > a:
        sum += A[n]
    # Подсчёт данных по фильтру
print(sum)
