"""4. Створіть масив з п'яти прізвищ і виведіть на екран ті з них, які
починаються з певної букви, яка вводиться з клавіатури."""
a = list()
for n in range(5):
    a.append(input())
#Создание масива
b = int(input("На яку букву прiзвища виводити:"))
b = b[0]
#Получение первой буквы введённых данных
for n in a:
    if n[0] == b:
        print(n)
#Вывод результатов