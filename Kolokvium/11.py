"""11. Дані про температуру води на Чорноморському узбережжі за декаду
вересня зберігаються в масиві. Визначити, скільки за цей час було днів, придатних для
купання."""
A = [15,18,7,4,1,-6,-10,-17,-12,-25]
# Создание масива
sum = 0
pri = int(input("Введiть температуру придатну для купання:"))
# Ввод температуры пригодной для купания пользователя
for n in range(len(A)):
    if A[n] >=pri:
        sum+=1
# Подсчёт данных
print(sum)